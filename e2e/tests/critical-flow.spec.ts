/**
 * Critical flow E2E tests
 *
 * Tests the full manager → staff lifecycle:
 *   login → create staff → assign task → (staff completes task) → verify KPI updates
 *
 * Prerequisites (set via env vars or .env.test):
 *   WEB_URL          - Web dashboard URL  (default: http://localhost:3000)
 *   MANAGER_PHONE    - Manager phone used for login
 *   MANAGER_PASSWORD - Manager password
 *   BACKEND_URL      - Backend API URL    (default: http://localhost:8080)
 *
 * Run:
 *   npm test
 *   WEB_URL=http://staging.example.com npm test
 */

import { test, expect, Page, request } from '@playwright/test';
import { LoginPage } from '../pages/LoginPage';
import { EmployeesPage } from '../pages/EmployeesPage';
import { TasksPage } from '../pages/TasksPage';
import { KpiPage } from '../pages/KpiPage';

const MANAGER_PHONE    = process.env.MANAGER_PHONE    ?? '0000000001';
const MANAGER_PASSWORD = process.env.MANAGER_PASSWORD ?? 'TestPass123!';
const BACKEND_URL      = process.env.BACKEND_URL      ?? 'http://localhost:8080';

// Unique per-run suffix prevents collisions in parallel CI environments
const RUN_ID = Date.now().toString().slice(-6);
const STAFF_PHONE   = `999${RUN_ID}`;
const STAFF_NAME    = `E2E Staff ${RUN_ID}`;
const STAFF_PASS    = 'StaffPass123!';
const TASK_TITLE    = `E2E Task ${RUN_ID}`;

// ---- SUITE 1: Authentication ----

test.describe('Authentication', () => {
  test('unauthenticated user is redirected to /login', async ({ page }) => {
    await page.goto('/dashboard');
    await expect(page).toHaveURL(/\/login/, { timeout: 8_000 });
  });

  test('manager can log in and reach dashboard', async ({ page }) => {
    const loginPage = new LoginPage(page);
    await loginPage.goto();
    await loginPage.loginAsManager(MANAGER_PHONE, MANAGER_PASSWORD);
    await expect(page).toHaveURL(/\/dashboard/);
  });

  test('invalid credentials shows error', async ({ page }) => {
    const loginPage = new LoginPage(page);
    await loginPage.goto();
    await page.getByLabel(/phone/i).fill('0000000000');
    await page.getByLabel(/password/i).fill('wrongpass');
    await page.getByRole('button', { name: /sign in|login/i }).click();
    await expect(page.getByText(/invalid|error|incorrect/i)).toBeVisible({ timeout: 8_000 });
  });
});

// ---- SUITE 2: Staff Management ----

test.describe('Staff management', () => {
  let page: Page;

  test.beforeAll(async ({ browser }) => {
    page = await browser.newPage();
    const loginPage = new LoginPage(page);
    await loginPage.goto();
    await loginPage.loginAsManager(MANAGER_PHONE, MANAGER_PASSWORD);
  });

  test.afterAll(() => page.close());

  test('manager can create a new staff member', async () => {
    const employeesPage = new EmployeesPage(page);
    await employeesPage.goto();
    await employeesPage.createStaff({
      phone: STAFF_PHONE,
      displayName: STAFF_NAME,
      password: STAFF_PASS,
    });
    await employeesPage.goto();   // refresh list
    await employeesPage.staffExists(STAFF_NAME);
  });

  test('employees page shows paginated list', async () => {
    const employeesPage = new EmployeesPage(page);
    await employeesPage.goto();
    // Page should render without error (list or empty state)
    await expect(page.locator('main, [data-testid="employees-container"]')).toBeVisible();
  });
});

