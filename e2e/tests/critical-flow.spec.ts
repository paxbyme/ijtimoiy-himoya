// ---- SUITE 4: Task Completion via API (staff role) ----

test.describe('Task completion and KPI update', () => {
  /**
   * Staff-side completion is done via API call (we simulate the mobile app).
   * We then verify that the KPI dashboard reflects the change as manager.
   */

  test('staff completing a task triggers KPI update (API-level)', async () => {
    // 1. Authenticate as staff
    const apiCtx = await request.newContext({ baseURL: BACKEND_URL });

    const authResp = await apiCtx.post('/api/auth/verify', {
      data: { phone: STAFF_PHONE, password: STAFF_PASS },
    });

    if (!authResp.ok()) {
      // Staff login may fail if running against a fresh env without real Firebase.
      // Skip instead of failing the suite.
      test.skip(true, 'Staff account not available in this env — skipping completion test');
    }

    const { data: { token, uid } } = await authResp.json();

    // 2. Get staff's tasks
    const tasksResp = await apiCtx.get('/api/tasks', {
      headers: { Authorization: `Bearer ${token}` },
    });
    expect(tasksResp.ok()).toBeTruthy();

    const tasksBody = await tasksResp.json();
    const tasks: Array<{ id: string; title: string; status: string }> =
        tasksBody.data?.content ?? tasksBody.data ?? [];

    const target = tasks.find(t => t.title === TASK_TITLE && t.status === 'PENDING');
    if (!target) {
      test.skip(true, `Task "${TASK_TITLE}" not found or already completed`);
    }

    // 3. Complete the task
    const completeResp = await apiCtx.put(`/api/tasks/${target!.id}/complete`, {
      headers: { Authorization: `Bearer ${token}` },
    });
    expect(completeResp.ok()).toBeTruthy();

    const completed = await completeResp.json();
    expect(completed.data.status).toBe('COMPLETED');

    await apiCtx.dispose();
  });

  test('KPI dashboard shows staff after task completion', async ({ page }) => {
    const loginPage = new LoginPage(page);
    await loginPage.goto();
    await loginPage.loginAsManager(MANAGER_PHONE, MANAGER_PASSWORD);

    const kpiPage = new KpiPage(page);
    await kpiPage.goto();

    // Staff should appear in rankings once KPI is calculated
    await kpiPage.staffAppearsInRankings(STAFF_NAME);
  });
});

